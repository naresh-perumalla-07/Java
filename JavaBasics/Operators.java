package JavaBasics;

public class Operators {
    public static void main(String[] args) {
        int a = 10, b = 5;

        // Arithmetic
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));

        // Comparison
        System.out.println("a > b? " + (a > b));
        System.out.println("a == 10? " + (a == 10));

        // Logical
        System.out.println("a > 0 && b > 0? " + (a > 0 && b > 0));
        System.out.println("a > 0 || b < 0? " + (a > 0 || b < 0));
    }
    
}
